package br.com.brunomilitzer.trainings.thymeleaf.model;

public class Student {

    private String name;

    private int score;

    public String getName() {
        return this.name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public int getScore() {
        return this.score;
    }

    public void setScore(final int score) {
        this.score = score;
    }
}
